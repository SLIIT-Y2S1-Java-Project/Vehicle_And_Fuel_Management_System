package Model;

public class StudentModel {
	private int stdId;
	private String stdName;
	private String stdEmail;
	private String stdUser;
	private String stdPass;
	
	public int getStdId() {
		return stdId;
	}
	public String getStdName() {
		return stdName;
	}
	public String getStdEmail() {
		return stdEmail;
	}
	public String getStdUser() {
		return stdUser;
	}
	public String getStdPass() {
		return stdPass;
	}
	
	
	public void setStdId(int stdId) {
		this.stdId = stdId;
	}
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	public void setStdEmail(String stdEmail) {
		this.stdEmail = stdEmail;
	}
	public void setStdUser(String stdUser) {
		this.stdUser = stdUser;
	}
	public void setStdPass(String stdPass) {
		this.stdPass = stdPass;
	}
	
	
}
